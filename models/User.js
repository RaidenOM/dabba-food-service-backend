const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
  },
  password: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
    unique: true,
  },
  type: {
    type: String,
    enum: ["customer", "admin", "delivery"],
  },
});

module.exports = mongoose.model("User", UserSchema);
